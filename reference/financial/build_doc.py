NAVY = RGBColor(0x1F, 0x38, 0x64)


def _setfont(run, size=14, bold=False, color=None):
    run.font.name = FONT
    run.font.size = Pt(size)
    run.bold = bold
    if color:
        run.font.color.rgb = color
    rpr = run._element.get_or_add_rPr()
    rf = rpr.get_or_add_rFonts()
    rf.set(qn("w:cs"), FONT)
    rf.set(qn("w:ascii"), FONT)
    rf.set(qn("w:hAnsi"), FONT)


def h(text, size=16, space_before=10):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(space_before)
    p.paragraph_format.space_after = Pt(4)
    _setfont(p.add_run(text), size, True, NAVY)
    return p


def para(text, size=14, bold=False, align=None, after=4):
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(after)
    if align:
        p.alignment = align
    _setfont(p.add_run(text), size, bold)
    return p


def bullet(text, size=14):
    p = doc.add_paragraph(style="List Bullet")
    _setfont(p.add_run(text), size)
    return p


def table(headers, rows, widths=None):
    t = doc.add_table(rows=1, cols=len(headers))
    t.style = "Light Grid Accent 1"
    for i, hd in enumerate(headers):
        c = t.rows[0].cells[i]
        c.paragraphs[0].clear()
        _setfont(c.paragraphs[0].add_run(hd), 13, True)
    for row in rows:
        cells = t.add_row().cells
        for i, v in enumerate(row):
            cells[i].paragraphs[0].clear()
            _setfont(cells[i].paragraphs[0].add_run(str(v)), 13)
    if widths:
        for r in t.rows:
            for i, w in enumerate(widths):
                r.cells[i].width = Inches(w)
    return t


